// Language management
const I18N = {
  current: localStorage.getItem("vettly_lang") || "en",
  
  set(lang) {
    this.current = lang;
    localStorage.setItem("vettly_lang", lang);
    this.apply();
  },
  
  t(key) {
    return LANG[this.current]?.[key] || LANG.en[key] || key;
  },
  
  apply() {
    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.getAttribute("data-i18n");
      const attr = el.getAttribute("data-i18n-attr");
      if (attr) {
        el.setAttribute(attr, this.t(key));
      } else {
        el.textContent = this.t(key);
      }
    });
    document.documentElement.lang = this.current;
    // Update active state on lang switcher
    document.querySelectorAll(".lang-btn").forEach(btn => {
      btn.classList.toggle("active", btn.dataset.lang === this.current);
    });
  }
};

// Language switcher HTML
function langSwitcherHTML() {
  return `<div class="lang-switcher">
    ${["en","tr","ru","fr"].map(l => 
      `<button class="lang-btn${I18N.current===l?" active":""}" data-lang="${l}" onclick="I18N.set('${l}')">${l.toUpperCase()}</button>`
    ).join("")}
  </div>`;
}

